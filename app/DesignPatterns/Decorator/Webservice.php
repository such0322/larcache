namespace App\DesignPatterns\Decorator;

/**
 * Description of Webservice
 *
 * @author admin
 */
class Webservice implements RendererInterface{
    //put your code here
    /**
     * @var mixed
     */
    protected $data;

    /**
     * @param mixed $data
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * @return string
     */
    public function renderData()
    {
        return $this->data;
    }
}
